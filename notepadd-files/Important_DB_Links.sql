  COUNT(*) AS row_count,
  t.avg_row_len,
  ROUND(COUNT(*) * t.avg_row_len / (1024*1024*1024), 2) AS estimated_size_gb
FROM &schema_name..&table_name y
JOIN all_tables t
  ON t.owner = UPPER('&schema_name')
AND t.table_name = UPPER('&table_name')
WHERE y.CR_DT BETWEEN DATE '2022-10-01' AND SYSDATE
GROUP BY t.avg_row_len;

